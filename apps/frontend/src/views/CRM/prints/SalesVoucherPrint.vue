<script setup lang="ts">
import { computed, ref } from 'vue';
import PrintContainer from '@/components/Print/PrintContainer.vue';
import { formatRocDate, formatInteger } from '@/utils/formatters';
import type { PrintOptions } from '@/utils/print';
import type { SalesVoucher, SalesVoucherItem } from '@/services/crm/sales-voucher.service';
import { voucherA5Layout, type PrintColumn } from './salesVoucherPrintLayout';

interface Props {
  voucher: SalesVoucher;
  items: SalesVoucherItem[];
}

const props = defineProps<Props>();
const printContainerRef = ref<InstanceType<typeof PrintContainer> | null>(null);
type VoucherPrintPageSize = Extract<NonNullable<PrintOptions['pageSize']>, 'A4' | 'A5'>;
const layout = voucherA5Layout;

const customerName = computed(
  () =>
    props.voucher.customer?.companyName ||
    props.voucher.customer?.companyShortName ||
    '未指定',
);
const voucherId = computed(() => props.voucher.id);
const voucherDate = computed(() => formatRocDate(props.voucher.createdAt));
const handler = computed(() => props.voucher.staff?.name || '未知');
const notes = computed(() => props.voucher.notes || '');
const amount = computed(() => Number(props.voucher.amount || 0));
const tax = computed(() => Number(props.voucher.tax || 0));
const totalAmount = computed(() => amount.value + tax.value);
const trimmedItems = computed(() => props.items.slice(0, layout.rows.maxRows));

const headerFields = computed(() => [
  { key: 'customerName', x: layout.header.customerName.x, y: layout.header.customerName.y, value: customerName.value },
  { key: 'customerId', x: layout.header.customerId.x, y: layout.header.customerId.y, value: props.voucher.customerId || '' },
  {
    key: 'shippingMethod',
    x: layout.header.shippingMethod.x,
    y: layout.header.shippingMethod.y,
    value: props.voucher.shippingMethod || '',
  },
  { key: 'voucherDate', x: layout.header.voucherDate.x, y: layout.header.voucherDate.y, value: voucherDate.value },
  { key: 'voucherId', x: layout.header.voucherId.x, y: layout.header.voucherId.y, value: voucherId.value },
  { key: 'orderId', x: layout.header.orderId.x, y: layout.header.orderId.y, value: props.voucher.orderId || '' },
]);

type RowEntry = { key: string; y: number; column: PrintColumn; value: string };
const rowEntries = computed<RowEntry[]>(() =>
  trimmedItems.value.flatMap((item, index) => {
    const y = layout.rows.startY + index * layout.rows.rowHeight;
    const amountValue = (item.quantity || 0) * (item.unitPrice || 0);
    return layout.columns.map((column) => {
      let value = '';
      switch (column.key) {
        case 'sequence':
          value = String(index + 1);
          break;
        case 'cadFile':
          value = item.cadFile || '';
          break;
        case 'customerFile':
          value = item.customerFile || '';
          break;
        case 'material':
          value = item.material || '';
          break;
        case 'thickness':
          value = item.thickness != null ? String(item.thickness) : '';
          break;
        case 'substitute':
          value = item.substitute || '';
          break;
        case 'quantity':
          value = formatInteger(item.quantity || 0);
          break;
        case 'unitPrice':
          value = formatInteger(item.unitPrice || 0);
          break;
        case 'amount':
          value = formatInteger(amountValue);
          break;
      }
      return {
        key: `${index}-${column.key}`,
        y,
        column,
        value,
      };
    });
  }),
);

const pointStyle = (x: number, y: number) => ({
  left: `${x + layout.offset.xMm}mm`,
  top: `${y + layout.offset.yMm}mm`,
});

const cellStyle = (x: number, y: number, width: number, align: PrintColumn['align']) => ({
  left: `${x + layout.offset.xMm}mm`,
  top: `${y + layout.offset.yMm}mm`,
  width: `${width}mm`,
  textAlign: align || 'left',
});

const getVoucherPrintStyles = (pageSize: VoucherPrintPageSize = 'A5'): string => {
  return `
    @page {
      size: A5 landscape !important;
      margin: 0;
    }

    .voucher-sheet {
      width: ${layout.page.widthMm}mm;
      min-height: ${layout.page.heightMm}mm;
      position: relative;
      background: #fff;
    }

    .data-layer {
      position: absolute;
      inset: 0;
      font-size: ${layout.font.basePt}pt;
      color: #111827;
      font-family: 'Microsoft JhengHei', 'PingFang TC', sans-serif;
    }

    .data-field {
      position: absolute;
      white-space: nowrap;
      line-height: 1.1;
      overflow: hidden;
      text-overflow: ellipsis;
    }

    .align-right {
      text-align: right;
    }
  `;
};

const print = (pageSize: VoucherPrintPageSize = 'A5') => {
  printContainerRef.value?.print({
    title: `銷貨單 ${pageSize} - ${props.voucher.id}`,
    styles: getVoucherPrintStyles(pageSize),
    pageSize,
  });
};

defineExpose({ print });
</script>

